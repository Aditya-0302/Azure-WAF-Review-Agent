class TestSecurityBoundaries:
    def test_missing_auth_returns_401(self, api_base: str) -> None:
        with httpx.Client() as client:
            r = client.get(f"{api_base}/v1/assessments", timeout=5)
        assert r.status_code == 401

    def test_invalid_token_returns_401(self, api_base: str) -> None:
        with httpx.Client() as client:
            r = client.get(
                f"{api_base}/v1/assessments",
                headers={"Authorization": "Bearer not.a.real.token"},
                timeout=5,
            )
        assert r.status_code == 401

    def test_cross_tenant_assessment_returns_404(
        self, api_base: str, auth_headers: dict[str, str]
    ) -> None:
        fake_assessment_id = str(uuid.uuid4())
        with httpx.Client() as client:
            r = client.get(
                f"{api_base}/v1/assessments/{fake_assessment_id}",
                headers=auth_headers,
                timeout=5,
            )
        # Either 404 (not found) or 403 (forbidden) is acceptable
        assert r.status_code in (404, 403)

    def test_security_headers_present(self, api_base: str) -> None:
        with httpx.Client() as client:
            r = client.get(f"{api_base}/healthz", timeout=5)
        assert r.headers.get("x-content-type-options") == "nosniff"
        assert r.headers.get("x-frame-options") == "DENY"

    def test_docs_disabled_in_production(self, api_base: str) -> None:
        with httpx.Client() as client:
            r = client.get(f"{api_base}/docs", timeout=5)
        # Docs must be disabled in production — 404 is expected
        assert r.status_code == 404
