"""End-to-end tests for the full assessment lifecycle.

These tests exercise the entire agent pipeline against a staging/test
environment.  They require real Azure infrastructure (or a Docker Compose
stack that emulates it) and are marked @pytest.mark.e2e + @pytest.mark.slow
so they only run in a dedicated E2E environment.

Environment variables required:
  E2E_API_BASE_URL      — Base URL of the deployed API (e.g. http://localhost:8080)
  E2E_BEARER_TOKEN      — A valid JWT (can be generated by a test identity)
  E2E_TENANT_ID         — UUID of the pre-seeded test tenant
  E2E_SUBSCRIPTION_ID   — UUID of the Azure subscription under test
  E2E_POLL_TIMEOUT_S    — Seconds to wait for assessment to complete (default 300)
  E2E_POLL_INTERVAL_S   — Polling interval in seconds (default 5)
"""

from __future__ import annotations

import os
import time
import uuid
from typing import Any

import httpx
import pytest

pytestmark = [pytest.mark.e2e, pytest.mark.slow]


# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------


def _env(key: str, default: str | None = None) -> str:
    val = os.getenv(key, default)
    if val is None:
        pytest.skip(f"E2E environment variable {key} not set")
    return val


@pytest.fixture(scope="session")
def api_base() -> str:
    return _env("E2E_API_BASE_URL", "http://localhost:8080")


@pytest.fixture(scope="session")
def auth_headers() -> dict[str, str]:
    token = _env("E2E_BEARER_TOKEN", None)
    return {"Authorization": f"Bearer {token}"}


@pytest.fixture(scope="session")
def tenant_id() -> str:
    return _env("E2E_TENANT_ID", None)


@pytest.fixture(scope="session")
def subscription_id() -> str:
    return _env("E2E_SUBSCRIPTION_ID", None)


@pytest.fixture(scope="session")
def poll_timeout() -> int:
    return int(_env("E2E_POLL_TIMEOUT_S", "300"))


@pytest.fixture(scope="session")
def poll_interval() -> int:
    return int(_env("E2E_POLL_INTERVAL_S", "5"))


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _post(
    client: httpx.Client,
    url: str,
    json: dict[str, Any],
    headers: dict[str, str],
) -> httpx.Response:
    r = client.post(url, json=json, headers=headers, timeout=30)
    r.raise_for_status()
    return r


def _get(
    client: httpx.Client,
    url: str,
    headers: dict[str, str],
) -> httpx.Response:
    r = client.get(url, headers=headers, timeout=30)
    r.raise_for_status()
    return r


def _poll_until(
    client: httpx.Client,
    url: str,
    headers: dict[str, str],
    *,
    terminal_statuses: set[str],
    timeout_s: int,
    interval_s: int,
) -> dict[str, Any]:
    deadline = time.monotonic() + timeout_s
    while time.monotonic() < deadline:
        r = _get(client, url, headers)
        payload = r.json()
        if payload.get("status") in terminal_statuses:
            return payload
        time.sleep(interval_s)
    pytest.fail(f"Timed out after {timeout_s}s waiting for {url} to reach {terminal_statuses}")


