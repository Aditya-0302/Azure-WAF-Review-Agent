# ---------------------------------------------------------------------------
# Full assessment lifecycle
# ---------------------------------------------------------------------------


class TestAssessmentLifecycle:
    def test_create_assessment_returns_201(
        self,
        api_base: str,
        auth_headers: dict[str, str],
        subscription_id: str,
    ) -> None:
        with httpx.Client() as client:
            r = _post(
                client,
                f"{api_base}/v1/assessments",
                json={
                    "subscription_ids": [subscription_id],
                    "pillar_filter": ["Security"],
                    "tag_filter": {},
                },
                headers=auth_headers,
            )
        assert r.status_code == 201
        body = r.json()
        assert "assessment_id" in body
        assert body["status"] in ("pending", "preparing")

    def test_assessment_reaches_completed_status(
        self,
        api_base: str,
        auth_headers: dict[str, str],
        subscription_id: str,
        poll_timeout: int,
        poll_interval: int,
    ) -> None:
        with httpx.Client() as client:
            create_r = _post(
                client,
                f"{api_base}/v1/assessments",
                json={
                    "subscription_ids": [subscription_id],
                    "pillar_filter": ["Security", "Reliability"],
                    "tag_filter": {},
                },
                headers=auth_headers,
            )
        assessment_id = create_r.json()["assessment_id"]

        with httpx.Client() as client:
            final = _poll_until(
                client,
                f"{api_base}/v1/assessments/{assessment_id}",
                auth_headers,
                terminal_statuses={"completed", "failed"},
                timeout_s=poll_timeout,
                interval_s=poll_interval,
            )

        assert (
            final["status"] == "completed"
        ), f"Assessment {assessment_id} ended in unexpected status: {final['status']}"

    def test_completed_assessment_has_findings(
        self,
        api_base: str,
        auth_headers: dict[str, str],
        subscription_id: str,
        poll_timeout: int,
        poll_interval: int,
    ) -> None:
        with httpx.Client() as client:
            create_r = _post(
                client,
                f"{api_base}/v1/assessments",
                json={
                    "subscription_ids": [subscription_id],
                    "pillar_filter": ["Security"],
                    "tag_filter": {},
                },
                headers=auth_headers,
            )
        assessment_id = create_r.json()["assessment_id"]

        with httpx.Client() as client:
            _poll_until(
                client,
                f"{api_base}/v1/assessments/{assessment_id}",
                auth_headers,
                terminal_statuses={"completed"},
                timeout_s=poll_timeout,
                interval_s=poll_interval,
            )
            findings_r = _get(
                client,
                f"{api_base}/v1/assessments/{assessment_id}/findings",
                auth_headers,
            )

        body = findings_r.json()
        assert "items" in body
        assert "total" in body
        # For any real subscription, there should be at least some findings
        assert body["total"] >= 0  # >= 0 is deliberately permissive

    def test_completed_assessment_has_report_url(
        self,
        api_base: str,
        auth_headers: dict[str, str],
        subscription_id: str,
        poll_timeout: int,
        poll_interval: int,
    ) -> None:
        with httpx.Client() as client:
            create_r = _post(
                client,
                f"{api_base}/v1/assessments",
                json={
                    "subscription_ids": [subscription_id],
                    "pillar_filter": ["Security"],
                    "tag_filter": {},
                },
                headers=auth_headers,
            )
        assessment_id = create_r.json()["assessment_id"]

        with httpx.Client() as client:
            _poll_until(
                client,
                f"{api_base}/v1/assessments/{assessment_id}",
                auth_headers,
                terminal_statuses={"completed"},
                timeout_s=poll_timeout,
                interval_s=poll_interval,
            )
            report_r = _get(
                client,
                f"{api_base}/v1/assessments/{assessment_id}/report",
                auth_headers,
            )

        body = report_r.json()
        assert "pdf_url" in body or "xlsx_url" in body

        # Verify the URL is a time-limited SAS URL, not a stored path
        for key in ("pdf_url", "xlsx_url"):
            if key in body and body[key]:
                url = body[key]
                assert url.startswith("https://")
                # SAS URLs contain 'sig=' parameter
                assert "sig=" in url, f"{key} is not a SAS URL"


